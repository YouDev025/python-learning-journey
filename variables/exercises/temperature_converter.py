# Create a variable celsius = 25
# Convert it to Fahrenheit using variables
# Formula: fahrenheit = (celsius * 9/5) + 32
# Print the result

# ANSWER

celsius = 25
fahrenheit = (celsius * 9 / 5) + 32
print(f"The celsius value is {celsius}")
print(f"The fahrenheit value is {fahrenheit}")