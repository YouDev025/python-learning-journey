# Convert False into an integer and print the result.

# Boolean value False
F = False

# Print original value
print(f"The value of F is '{F}'")

# Print original type (bool)
print(f"The type of F is '{type(F)}'")

# Convert boolean to integer
# In Python: False -> 0
F = int(F)

# Print converted value
print(f"The value of F is '{F}'")

# Print new type (int)
print(f"The type of F is '{type(F)}'")