# Convert the integer 7 into a float and print the result.

my_int = 7
print(f"The Int is {my_int} and the type is '{type(my_int)}'")

my_float = float(my_int)
print(f"The Float is {my_float} and the type is '{type(my_float)}'")

# When converting an integer to a float:
    # Python adds a decimal point .0 like 5 to 5.0
    # The value stays the same, only the type changes