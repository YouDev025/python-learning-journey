# a is a string that contains a numeric value
a = "10"

# b is an integer
b = 5

# Convert string "10" into integer 10
# int("10") -> 10
a = int(a)

# Add the two integers
c = a + b

# Print the result of the addition
print(f"{a} + {b} = {c}")