# Convert True into an integer and print the result.

# Boolean value True
B = True
print(f"The value of B is '{B}'")

# Show original type (bool)
print(f"The type of B is '{type(B)}'")

# Convert boolean to integer
# In Python: True -> 1, False -> 0
B = int(B)

# Print converted value
print(f"\nthe value of B is '{B}'")

# Show new type (int)
print(f"The type of B is '{type(B)}'")